# Chapter 3 working with lists

# list of bicycles
bicycles = ['trek', 'cannondale', 'redline', 'specialized']

# prints full list
print(bicycles)

# prints first item in list
print(bicycles[0])

# prints first item in list as a title (Capitalizes the word)
print(bicycles[0].title())

# printing other indexes in the list
print(bicycles[1])
print(bicycles[3])

# print last item in a list
print(bicycles[-1])

# individual values from list message
message = f"My first bicycle was a {bicycles[0].title()}."
print(message)

